#!/bin/bash

# تنظيف الشاشة ليكون العمل احترافياً
clear
echo "🚀 جاري فحص حالة المشروع في ورقلة..."

# 1. إضافة كل التغييرات
git add .

# 2. طلب عنوان التعديل من المستخدم
echo "------------------------------------------"
read -p "📝 ما هو عنوان هذا التعديل (Commit Message)؟ " message

# إذا كان العنوان فارغاً، نضع عنواناً افتراضياً
if [ -z "$message" ]; then
    message="Update from Cloud Shell $(date +'%Y-%m-%d %H:%M')"
fi

# 3. تسجيل التعديل محلياً
git commit -m "$message"

# 4. الرفع للسحاب
echo "------------------------------------------"
echo "☁️ جاري الرفع إلى GitHub..."
git push

# 5. النتيجة النهائية
if [ $? -eq 0 ]; then
    echo "------------------------------------------"
    echo "✅ تم الحفظ والرفع بنجاح يا ماستر!"
    echo "🔗 الرابط: https://github.com/ectroshop9/smart_hive"
else
    echo "❌ فشل الرفع! تأكد من اتصال الإنترنت أو الـ Token."
fi
